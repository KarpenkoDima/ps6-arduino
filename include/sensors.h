#ifndef SENSORS_H
#define SENSORS_H

#include <Arduino.h>

// В этом проекте на плате нет датчиков среды (температуры/влажности).
// Файл sensors.* отвечает за управление двумя световыми индикаторами:
//   — LED_ALARM: горит, когда будильник включён;
//   — LED_RUN:   горит, когда работает таймер или секундомер.
// Во время сработавшего будильника обе лампочки мигают противофазой.
enum LedId {
    LED_ALARM,
    LED_RUN
};

// Настроить пины обоих светодиодов на выход.
void leds_init();

// Установить состояние конкретного светодиода.
void led_set(LedId id, bool on);

// Запустить/остановить «звон» обоих светодиодов противофазой.
void leds_alarm_blink(bool active);

// Должно вызываться из loop() — обновляет мигание, когда оно запущено.
void leds_tick();

#endif
